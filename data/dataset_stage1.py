"""
dataset_stage1.py — Dataset PyTorch for Stage 1 (DreamBooth + LoRA for [V]).

Manages the instance images and prior images used for the prior preservation loss.
"""

import json
import random
from pathlib import Path
from typing import Optional

import torch
from PIL import Image
from torch.utils.data import Dataset
from torchvision import transforms


# Standard Transformation for SD 1.5 
# SD 1.5 accepts 512×512 images normalized in [-1, 1]

def build_transform(size: int = 512, augment: bool = True) -> transforms.Compose:
    """
    Builds the transformation pipeline for training images.

    Args:
        size: target dimension (default 512 for SD 1.5)
        augment: if True, adds random horizontal flip
    """
    tfms = [
        transforms.Resize(size, interpolation=transforms.InterpolationMode.BILINEAR),
        transforms.CenterCrop(size),
    ]
    if augment:
        tfms.append(transforms.RandomHorizontalFlip())
    tfms += [
        transforms.ToTensor(),
        transforms.Normalize([0.5], [0.5]),   
    ]
    return transforms.Compose(tfms)


# Dataset instance (product images)

class Stage1InstanceDataset(Dataset):
    """
    Load clean product images for Stage 1.

    Every item returns:
        {
          "pixel_values": Tensor [3, H, W],   # normalized image
          "input_ids":    Tensor [L],          # token IDs of the instance prompt
        }
    """

    def __init__(
        self,
        split_path: str,
        tokenizer,
        token_V: str = "sks",
        category: str = "bottle",
        image_size: int = 512,
        augment: bool = True,
    ):
        """
        Args:
            split_path: path to JSON product data from splits.py
            tokenizer:  tokenizer of SD 1.5 (CLIPTokenizer)
            token_V:    special token for the product (e.g., "sks")
            category:   MVTec category name 
            image_size: image dimension 
            augment:    random flip for data augmentation
        """
        with open(split_path) as f:
            split_data = json.load(f)

        self.image_paths = split_data["stage1"]
        self.transform = build_transform(image_size, augment)
        self.tokenizer = tokenizer

        self.prompt = f"a photo of a {token_V} {category}"


        self.input_ids = tokenizer(
            self.prompt,
            padding="max_length",
            truncation=True,
            max_length=tokenizer.model_max_length,
            return_tensors="pt",
        ).input_ids[0]

        print(f"[Stage1Dataset] {len(self.image_paths)} images | prompt: '{self.prompt}'")

    def __len__(self) -> int:
        return len(self.image_paths)

    def __getitem__(self, idx: int) -> dict:
        img_path = self.image_paths[idx]
        image = Image.open(img_path).convert("RGB")
        pixel_values = self.transform(image)
        return {
            "pixel_values": pixel_values,
            "input_ids": self.input_ids,
        }


# Dataset prior 

class Stage1PriorDataset(Dataset):
    """
    Dataset for the prior images used in prior preservation loss.

    The prior images are generated by the base model with the prompt
    "a photo of a {category}" (without the special token [V]) and saved on disk
    before training. See train/generate_prior_images.py.

    Every item returns the same structure as Stage1InstanceDataset.
    """

    def __init__(
        self,
        prior_images_dir: str,
        tokenizer,
        category: str = "bottle",
        image_size: int = 512,
    ):
        """
        Args:
            prior_images_dir: directory containing the pre-generated prior images
            tokenizer:        tokenizer of SD 1.5
            category:         category name (for the prior prompt)
            image_size:       image dimension
        """
        prior_dir = Path(prior_images_dir)
        self.image_paths = sorted(
            list(prior_dir.glob("*.png")) + list(prior_dir.glob("*.jpg"))
        )

        if len(self.image_paths) == 0:
            raise FileNotFoundError(
                f"No prior images found in {prior_dir}. "
                "Run train/generate_prior_images.py first"
            )

        self.transform = build_transform(image_size, augment=False)
        self.tokenizer = tokenizer

        # Prompt prior: "a photo of a bottle" (without the special token)
        self.prompt = f"a photo of a {category}"

        self.input_ids = tokenizer(
            self.prompt,
            padding="max_length",
            truncation=True,
            max_length=tokenizer.model_max_length,
            return_tensors="pt",
        ).input_ids[0]

        print(f"[PriorDataset] {len(self.image_paths)} prior images | prompt: '{self.prompt}'")

    def __len__(self) -> int:
        return len(self.image_paths)

    def __getitem__(self, idx: int) -> dict:
        img_path = self.image_paths[idx]
        image = Image.open(img_path).convert("RGB")
        pixel_values = self.transform(image)
        return {
            "pixel_values": pixel_values,
            "input_ids": self.input_ids,
        }


# Combined Dataset  (instance + prior interleaved)

class DreamBoothDataset(Dataset):
    """
    Combine instance and prior datasets in the same batch.

    The training loop of DreamBooth uses batches that contain
    both instance images and prior images. This dataset
    interleaves them so that the DataLoader receives them together.

    The final loss is:
        L = L_instance + prior_loss_weight * L_prior
    """

    def __init__(
        self,
        instance_dataset: Stage1InstanceDataset,
        prior_dataset: Optional[Stage1PriorDataset] = None,
    ):
        self.instance_dataset = instance_dataset
        self.prior_dataset = prior_dataset
        self.use_prior = prior_dataset is not None

        
        self._len = max(
            len(instance_dataset),
            len(prior_dataset) if prior_dataset else 0,
        )

    def __len__(self) -> int:
        return self._len

    def __getitem__(self, idx: int) -> dict:
        inst_idx = idx % len(self.instance_dataset)
        item = self.instance_dataset[inst_idx]

        if self.use_prior:
            prior_idx = idx % len(self.prior_dataset)
            prior_item = self.prior_dataset[prior_idx]
            item["prior_pixel_values"] = prior_item["pixel_values"]
            item["prior_input_ids"] = prior_item["input_ids"]

        return item


def collate_fn(examples: list) -> dict:
    """
    Collation function for the DataLoader.
    Handles the optional presence of prior items.
    """
    pixel_values = torch.stack([e["pixel_values"] for e in examples])
    input_ids = torch.stack([e["input_ids"] for e in examples])

    batch = {
        "pixel_values": pixel_values.to(memory_format=torch.contiguous_format).float(),
        "input_ids": input_ids,
    }

    if "prior_pixel_values" in examples[0]:
        prior_pv = torch.stack([e["prior_pixel_values"] for e in examples])
        prior_ids = torch.stack([e["prior_input_ids"] for e in examples])
        # Concatenate instance and prior along the batch dimension
        # The training loop will then split them in half to compute the two losses
        batch["pixel_values"] = torch.cat([pixel_values, prior_pv], dim=0)
        batch["input_ids"] = torch.cat([input_ids, prior_ids], dim=0)

    return batch
